public class Addition
{
    //concatenates and add two numbers and prints the result 
    
    public static void main (String[] args)
    {
        System.out.println("24 and 45 concatenated: " + 24 + 45);
        System.out.println("24 and 45 added: " + (24 + 45));
    }
}


